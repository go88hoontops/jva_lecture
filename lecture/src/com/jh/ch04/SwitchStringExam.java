package com.jh.ch04;

public class SwitchStringExam {

	public static void main(String[] args) {

		String position = "일반ㅇㅇㅇ";
		
		
		switch(position) {
		
		case "부장":
			System.out.println("월 700 오우ㅉ!");
			break;
			
		case "과장":
			System.out.println("월 500 오우ㅆ");
			break;
		default:
			System.out.println("월 300 오우예");
			
		
		
		}
		
		
	}

}
